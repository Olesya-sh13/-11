#!/bin/bash
# =============================================================================
# Скрипт развёртывания приложения «Контейнер журналирования событий доступа»
# на платформе Replit.
#
# Что делает этот скрипт:
#   1. Проверяет наличие Python 3.10+
#   2. Устанавливает все зависимости через Poetry
#   3. Применяет миграции базы данных Django
#   4. Создаёт пользователей из policy.json
#   5. Генерирует ключ шифрования (если ещё не существует)
#   6. Создаёт необходимые директории для логов
#   7. Запускает сервер Gunicorn на порту 5000
#
# Использование:
#   chmod +x setup.sh
#   ./setup.sh          # Развернуть и запустить сервер
#   ./setup.sh --no-run # Только подготовить окружение, без запуска сервера
# =============================================================================

set -e  # Прерывать выполнение при любой ошибке

# --- Цвета для вывода ---
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'  # Сброс цвета

ok()   { echo -e "${GREEN}[OK]${NC}  $1"; }
info() { echo -e "${YELLOW}[..]${NC}  $1"; }
fail() { echo -e "${RED}[!!]${NC}  $1"; exit 1; }

echo ""
echo "=========================================="
echo "  Развёртывание Security Logger App"
echo "=========================================="
echo ""

# =============================================================================
# ШАГ 1: Проверка версии Python
# =============================================================================
info "Проверяем Python..."

if ! command -v python3 &>/dev/null; then
    fail "Python 3 не найден. Убедитесь, что модуль python-3.10 включён в .replit"
fi

PY_VERSION=$(python3 -c "import sys; print(f'{sys.version_info.major}.{sys.version_info.minor}')")
PY_MAJOR=$(python3 -c "import sys; print(sys.version_info.major)")
PY_MINOR=$(python3 -c "import sys; print(sys.version_info.minor)")

if [ "$PY_MAJOR" -lt 3 ] || ([ "$PY_MAJOR" -eq 3 ] && [ "$PY_MINOR" -lt 10 ]); then
    fail "Требуется Python 3.10 или выше. Найдена версия: $PY_VERSION"
fi

ok "Python $PY_VERSION — подходит"

# =============================================================================
# ШАГ 2: Установка зависимостей
# =============================================================================
info "Устанавливаем зависимости через Poetry..."

if ! command -v poetry &>/dev/null; then
    info "Poetry не найден, устанавливаем через pip..."
    pip install poetry --quiet || fail "Не удалось установить Poetry"
fi

poetry install --no-interaction --quiet \
    || fail "Ошибка установки зависимостей. Проверьте pyproject.toml"

ok "Зависимости установлены"

# =============================================================================
# ШАГ 3: Применение миграций базы данных
# =============================================================================
info "Применяем миграции Django..."

python3 manage.py migrate --no-input \
    || fail "Ошибка при выполнении миграций базы данных"

ok "Миграции применены"

# =============================================================================
# ШАГ 4: Создание пользователей из policy.json
# =============================================================================
info "Создаём пользователей из policy.json..."

python3 manage.py create_users_from_policy \
    || fail "Ошибка при создании пользователей"

ok "Пользователи готовы"

# =============================================================================
# ШАГ 5: Генерация ключа шифрования (если отсутствует)
# =============================================================================
KEY_FILE="logger_app/data/secret.key"
info "Проверяем ключ шифрования..."

if [ ! -f "$KEY_FILE" ]; then
    info "Ключ не найден — генерируем новый..."
    python3 -c "
from logger_app.encryption import generate_key
generate_key()
print('Ключ Fernet сгенерирован и сохранён в logger_app/data/secret.key')
" || fail "Ошибка генерации ключа шифрования"
    ok "Ключ шифрования создан"
else
    ok "Ключ шифрования уже существует"
fi

# =============================================================================
# ШАГ 6: Создание директорий для логов
# =============================================================================
info "Проверяем директории для логов..."

mkdir -p logger_app/logs
ok "Директория logger_app/logs готова"

# =============================================================================
# ШАГ 7: Итоговая сводка
# =============================================================================
echo ""
echo "=========================================="
echo -e "  ${GREEN}Развёртывание завершено успешно!${NC}"
echo "=========================================="
echo ""
echo "  Адрес приложения:  http://0.0.0.0:5000"
echo ""
echo "  Тестовые учётные записи:"
echo "  ┌─────────────────────┬────────────────┬─────────────────────────┐"
echo "  │ Логин               │ Пароль         │ Роль                    │"
echo "  ├─────────────────────┼────────────────┼─────────────────────────┤"
echo "  │ admin               │ Admin2024!     │ Администратор           │"
echo "  │ Аудитор             │ audit2024      │ Аудитор                 │"
echo "  │ Геолог              │ geo2024        │ Геолог                  │"
echo "  │ Петрофизик          │ petro2024      │ Петрофизик              │"
echo "  │ Инженер-разработчик │ eng2024        │ Инженер-разработчик     │"
echo "  │ Оператор            │ oper2024       │ Оператор                │"
echo "  └─────────────────────┴────────────────┴─────────────────────────┘"
echo ""

# =============================================================================
# ШАГ 8: Запуск сервера (если не передан флаг --no-run)
# =============================================================================
if [ "$1" != "--no-run" ]; then
    info "Запускаем Gunicorn на порту 5000..."
    echo ""
    exec gunicorn -c gunicorn.conf.py django_project.wsgi:application
fi
